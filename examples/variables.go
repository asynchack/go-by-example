package main

import "fmt"

func main() {
	var a string = "initial"
	fmt.Println(a)

	var b = 1
	fmt.Println(b)

	var c, d int = 6, 233
	fmt.Println(c, d)

	var e float32 = 3
	fmt.Println(e)

	f := "apple"
	fmt.Println(f)

	g, h := "apple", "banana"
	fmt.Println(g, h)

	var good bool
	fmt.Println(good)
}
